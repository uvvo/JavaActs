//Use any 2 numbers for the Assignment operators print output
public class AssignmentOperators
{
public static void main(String[] args) {
       /// Declaring variables 
        int num1 = 5, num2 = 10; 
        int res;
        System.out.println("Value of number1 = " + num1); 
        System.out.println("Value of number2 = " + num2); 
        
        // assign value using =
        num1 = num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 == is " + num1);
        
        // assign value using +=
        num1 += num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 += is " + num1);
        
        // assign value using -=
        num1 -= num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 -= is " + num1);
        
        // assign value using *=
        num1 *= num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 *= is " + num1);
        
        // assign value using  /=
        num1 /= num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 /= is " + num1);
        
        // Assign value using *=
        num1 %= num2; 
        // Displaying the assigned values 
        System.out.println("Value of num1 %= is " + num1); 
    } 
} 
   
    
    
    
 
  